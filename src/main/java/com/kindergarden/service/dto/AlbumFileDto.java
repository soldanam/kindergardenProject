package com.kindergarden.service.dto;


public class AlbumFileDto {
	private int albumFileId;
	private String albumFileName;
	private String albumFileUrl;
	private int albumId;
	public int getAlbumFileId() {
		return albumFileId;
	}
	public void setAlbumFileId(int albumFileId) {
		this.albumFileId = albumFileId;
	}
	public String getAlbumFileName() {
		return albumFileName;
	}
	public void setAlbumFileName(String albumFileName) {
		this.albumFileName = albumFileName;
	}
	public String getAlbumFileUrl() {
		return albumFileUrl;
	}
	public void setAlbumFileUrl(String albumFileUrl) {
		this.albumFileUrl = albumFileUrl;
	}
	public int getAlbumId() {
		return albumId;
	}
	public void setAlbumId(int albumId) {
		this.albumId = albumId;
	}
	
	
	
}