package com.kindergarden.service.dto;

public class ClassDto {
	private int classId;
	private String className;
	private int classAge;
	private int classCnt;
	public int getClassId() {
		return classId;
	}
	public void setClassId(int classId) {
		this.classId = classId;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public int getClassAge() {
		return classAge;
	}
	public void setClassAge(int classAge) {
		this.classAge = classAge;
	}
	public int getClassCnt() {
		return classCnt;
	}
	public void setClassCnt(int classCnt) {
		this.classCnt = classCnt;
	}
	
	
	

}
